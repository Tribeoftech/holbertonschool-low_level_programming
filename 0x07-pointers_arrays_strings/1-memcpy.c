/**
 * _memcpy - asdf adf ads fasdf asdf
 * @dest: adsf ads fasd sdfghjk
 * @src: asdfghjkl
 * @n: nasdfghjk sdfghjk dfghjk fghj
 * Return: pfghjkdfghjfghjk
 */
char *_memcpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < n; i++)
		dest[i] = src[i];
	return (dest);
}
